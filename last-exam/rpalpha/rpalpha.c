void	ft_putchar(char c)
{
	write(1,&c,1);
}
int main(int argc, char **argv)
{
	int i = 0;
	int r = 0;
	while(argv[1][i])
	{
		r = 1;
		if (argv[1][i] >= 'a' && argv[1][i] <= 'z')
			r = argv[1][i] - 'a' + 1;
		if (argv[1][i] >= 'A'&& argv[1][i] <= 'Z')
			r = argv[1][i] - 'A' + 1;
		while(0 < r)
		{
			ft_putchar(argv[1][i]);
			r--;
		}
		i++;
	}
}		 
