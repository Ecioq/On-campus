int main(int argc, char **argv)
{
	int	i = 0;
	int	in_word = 0;
	while(argv[1][i] == 32)
	{
		i++;
	}
	while(argv[1][i])
	{
		if (argv[1][i] == 32)
		{
			in_word = 1;
			i++;
		}
		else if(in_word == 1)
		{
			write(1,"   ",3);
			in_word = 0;
		}
		else
		{
			write(1, &argv[1][i],1);
			i++;
		}
	}
}



